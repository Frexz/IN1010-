public class Areal {
    public static void main(String[] args) {
        int bredde;
        int hoyde;

        bredde = 10;
        hoyde = 20;

        System.out.println("Bredde: " + bredde);
        System.out.println("Hoyde: " + hoyde);

        int areal = bredde * hoyde;
        System.out.println("Areal: " + areal);
    }
}
